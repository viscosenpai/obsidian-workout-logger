// src/utils.ts
function calculateVolume(weight, reps) {
  return Math.round(weight * reps);
}
function calculate1RM(weight, reps) {
  if (reps === 0)
    return 0;
  if (reps === 1)
    return weight;
  const oneRM = weight * (1 + reps / 30);
  return Math.round(oneRM * 10) / 10;
}
var EQUIPMENT_METS = {
  Barbell: 6,
  Dumbbell: 6,
  Machine: 5,
  Bodyweight: 3.8,
  Other: 5
};
function calculateCalories(mets, weight, duration) {
  return Math.round(mets * weight * duration * 1.05);
}
var calculateWalkingCalorie = (weight, kph, inclinePercent, minutes) => {
  const v = kph / 0.06;
  const g = inclinePercent / 100;
  const vo2 = 0.1 * v + 1.8 * v * g + 3.5;
  return vo2 * weight / 1e3 * 5 * minutes;
};

