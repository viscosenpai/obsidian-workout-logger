var WorkoutLoggerPlugin = class extends import_obsidian5.Plugin {
  constructor() {
    super(...arguments);
    this.settings = DEFAULT_SETTINGS;
  }
  async onload() {
    await this.loadSettings();
    this.registerView(
      DASHBOARD_VIEW_TYPE,
      (leaf) => new DashboardView(leaf, this)
    );
    const ribbonIconEl = this.addRibbonIcon(
      "bar-chart-2",
      "Workout dashboard",
      () => {
        this.activateDashboard();
      }
    );
    ribbonIconEl.addClass("workout-logger-ribbon-class");
    this.addCommand({
      id: "open-dashboard",
      name: "Open dashboard",
      callback: () => {
        this.activateDashboard();
      }
    });
    this.addCommand({
      id: "open-logger",
      name: "Open logger",
      callback: () => {
        new LoggerModal(this.app, this).open();
      }
    });
    this.addSettingTab(new WorkoutLoggerSettingTab(this.app, this));
  }
  onunload() {
  }
  async activateDashboard() {
    const { workspace } = this.app;
    let leaf = workspace.getLeavesOfType(DASHBOARD_VIEW_TYPE)[0];
    if (!leaf) {
      leaf = workspace.getLeaf(false);
      await leaf.setViewState({ type: DASHBOARD_VIEW_TYPE, active: true });
    }
    workspace.revealLeaf(leaf);
  }
  async loadSettings() {
    this.settings = Object.assign({}, DEFAULT_SETTINGS, await this.loadData());
  }
  async saveSettings() {
    await this.saveData(this.settings);
  }
};
